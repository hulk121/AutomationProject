package VariablesAndDatatypes;

public class Class2 {

	public static void main(String[] args) {
		//numbers
		int age=30;
        
		System.out.println(age);
		
		//characters
		char gender='M';
		System.out.println(gender);
		
		//true or false
		boolean flag=true;
		
		//decimal values
		double amt=899.09;
		
		//strings values
		String name="kalyani";
		
		// less used data types
		// numbers
		
		byte a=10;
		
		short b=20;
	
		
		//decimal
		float f=78.46f;
		
			
	}

}
